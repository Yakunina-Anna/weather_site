// Функция для отображения SVG
function showWeatherIcon(type) {
  const iconContainer = document.getElementById("weather-icon");
  const textContainer = document.getElementById("weather-text");

  // Проверяем, есть ли такой тип в массиве
  if (weatherData[type]) {
    iconContainer.innerHTML = weatherData[type].svg; // Вставляем SVG
    textContainer.textContent = weatherData[type].text; // Вставляем текст
  } else {
    iconContainer.innerHTML = "<p>Иконка не найдена!</p>"; // Сообщение об ошибке
    textContainer.textContent = "Неизвестно"; // Текст по умолчанию
  }
}
